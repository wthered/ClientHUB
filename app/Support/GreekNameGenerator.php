		public static function generate(): array {
			$first = static::$firstNames[array_rand(static::$firstNames)];
			$last  = static::$lastNames[array_rand(static::$lastNames)];

			$firstLatin = strtolower(strtr($first, static::$map));
			$lastLatin  = strtolower(strtr($last, static::$map));

			return [
				'first_name' => $first,
				'last_name'  => $last,
				'email'      => "$firstLatin.$lastLatin@" . fake()->freeEmailDomain(),
				'job_title'  => static::$jobTitles[array_rand(static::$jobTitles)],
			];
		}
